package com.twentyone;

public class PredefinedGoals {


        int preGoalID;
        String preGoal;

        public PredefinedGoals(int preGoalID, String preGoal) {
            this.preGoalID = preGoalID;
            this.preGoal = preGoal;
        }

        public int getPreGoalID() {
            return preGoalID;
        }

        public void setPreGoalID(int preGoalID) {
            this.preGoalID = preGoalID;
        }

        public String getPreGoal() {
            return preGoal;
        }

        public void setPreGoal(String preGoal) {
            this.preGoal = preGoal;
        }
    }
